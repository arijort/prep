import unittest

def find_rectangular_overlap(rect1, rect2):
    # Calculate the overlap between the two rectangles
    """ Data structure for the rectanble is a dictionary as follows:
        rect1 = {
            'left_x': 1,
            'bottom_y': 1,
            'width': 6,
            'height': 3,
        }
    """
    no_overlap = dict( {'left_x': None, 'bottom_y': None, 'width': None, 'height': None } )

    def find_overlap(coord_1, coord_2, measure_1, measure_2):
        """ Given 2 coordinate values on a 1 dimensional scale and 2 measures, find overlapping space.
        This function should work for both x (coord is left_x or rectangle) and y (coord is bottom of rectangle).
        The measure will be width (x) or height (y). """
        higher_start = max( coord_1, coord_2)
        lower_end =    min( coord_1 + measure_1, coord_2 + measure_2)
        if higher_start >= lower_end:
          return [None, None]
        return [ higher_start, lower_end - higher_start ]

    x_overlap = find_overlap(rect1['left_x'],   rect2['left_x'],   rect1['width'],  rect2['width'])
    y_overlap = find_overlap(rect1['bottom_y'], rect2['bottom_y'], rect1['height'], rect2['height'])
    if not all(x_overlap + y_overlap):
        return no_overlap
    return { 'left_x': x_overlap[0], 'width': x_overlap[1], 'bottom_y': y_overlap[0], 'height': y_overlap[1] }

